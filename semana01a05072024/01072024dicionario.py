#Dicionário

# Dicionários são estruturas de dados que mapeiam chaves a valores. São muito eficientes para buscas
# rápidas e associações de informações.

dicionario = {'chave1':1, 'chave2':2, 'chave3':3 }
print(dicionario)

#Exemplo:
#Operações comuns com dicionários: Acessar valor por chave:
print(dicionario ['chave1'])

#Acessar valor por chave: 
dicionario['chave4'] = 4
print(dicionario)