#Conjuntos

# Conjuntos, em Python, são coleções de elementos únicos e não ordenados. São úteis para realizar operações
# como união,interseção e diferença entre coleções.

# Operações comuns com conjuntos:
conjunto = {1, 2, 3, 4, 5}
print(conjunto)

#Adicionar elementos:
conjunto.add(6)
print(conjunto)

#Remover elementos:
conjunto.remove(2)
print(conjunto)
